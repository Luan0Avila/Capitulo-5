years_old = 90

if years_old < 2:
    print ('Você é um nenem!')
elif years_old >= 2 and years_old < 4:
    print ('Você é uma criança!')
elif years_old >= 4 and years_old < 13:
    print ('Você é um(a) garoto(a)!')
elif years_old >= 13 and years_old < 20:
    print ('Você é um adolescente!')
elif years_old >= 20 and years_old < 65:
    print ('Você é um adulto!')
else:
    print ('Você é um(a) idoso(a)!')